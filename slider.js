let slideShow = function(elementName,configurationObject) {

   let images = [];
    let curImage = 0;
    for (i = 0; i < elementName.childElementCount; i++) {
     images.push(elementName.children[i]);    
    }

    let nextSlide = function(elementId) {
        if(configurationObject[curImage].entry.fx == "fadein") {
     images[curImage].style.transform = "translate(0,0)";
     images[curImage].style.opacity = 1;
    } else if(configurationObject[curImage].entry.fx == "toleft"){    
        for (let i = 0; i < images.length; i++) { 
                images[i].style.transform = "translate(-100%,0)"; 
                images[i].style.opacity = "0";           
        };
        images[curImage].style.transform = "translate(0,0)";
        images[curImage].style.opacity = 1;
    };    
      curImage++;    
      if(curImage >= images.length ) curImage=0; 
      if(configurationObject[curImage].entry.fx == "fadein") {
        images[curImage].style.transform = "translate(0,0)";
        images[curImage].style.opacity =0;
      };
      if(configurationObject[curImage].entry.fx == "toleft") {
        images[curImage].style.transform = "translate(100%,0)";
        images[curImage].style.opacity =0;
      };
      window.setTimeout(nextSlide.bind(elementId), configurationObject[curImage].duration);
    };    
    nextSlide(elementName);   
  };

function Slider(elementName,configurationObject) {
    createDom(elementName,configurationObject);
    slideShow(elementName,configurationObject);
};
 
